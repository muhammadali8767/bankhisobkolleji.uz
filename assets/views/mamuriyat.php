    <h4 align="center">O’ZBEKISTON RESPUBLIKASI OLIY VA O’RTA MAXSUS TA’LIM VAZIRLIGI O’RTA MAXSUS KASB-HUNAR TA’LIMI MARKAZI
TOSHKENT SHAHAR O’RTA MAXSUS KASB-HUNAR TALIMI BOSHQARMASI
TOSHKENT BANK HISOB-KREDIT KOLLEJI</h4>

<p align="justify">Tashkent bank hisob-kredit kolleji О‘zbekiston Respublikasi Vazirlar Maxkamasinig 1995-yil 10-iyundagi 216-sonli qaroriga asosan О’zbekiston Respublikasi Markaziy banki qoshida tashkil etilgan.</p>


<p>2009 yil kollej to‘liq rekonstruksiya qilingan.</p>

<p align="justify">О’zbekiston Respublikasi Vazirlar Maxkamasinig 2009 yil 8 apreldagi 101- sonli qaroriga asosan 2010 yil yanvardan kollej O’rta maxsus kasb-hunar ta’limi markazi ixtiyoriga o’tkazilgan.</p>
<p align="justify">Tashkent Bank hisob- kredit kolleji yangi turdagi o’quv muassasasi bo ‘lib, о‘quvchilarni tanlagan mutaxassisliklari bo‘yicha davlat ta‘lim standartlari asosida kasbiy уо‘nalishiga qarab ularning qobilyatlari, ко‘nikmalari va extiyojlarini xisobga olgan holda quyidagi mutaxassisliklar bo’yicha kadrlar tayyorlanadi:</p>

<p>1.Bank ishi mutaxassisi — 3340701</p>
<p>2.Buxgalter — 3340901</p>
<p>3.Moliyachi — 3340601</p>

<p align="justify">Xozirgi paytda kollejda 120 dan ortiq о‘qituvchilar faoliyat ko’rsatib kelmoqda. Ulardan ko’pchiligi yuqori toyifali о‘qituvchilar, fan doktori va fan nomzodlaridir.</p>

<p align="justify">Kollejda tanlagan mutaxassislik bo‘yicha chuqur bilim olish uchun barcha sharoitlar yaratilgan. Kollej 3 qavatli о‘quv va mа‘muriy binolarda joylashgan.</p>

<p align="justify">Ma‘muriy binoda 27 mingdan ortiq kitob fondiga, elektron darsliklarga ega bo‘lgan axborot resurs markazi, 70 о‘rinli qiroatxona, faollar zali, sport-sog’lomlashtirish majmui, 230 o’ringa mo’ljallangan oshxona mav</p>
<p align="justify">O’quv binolaridagi auditoriyalardan samarali foydalanish maqsadida barcha fanlar bo‘yicha fizika, ximya, biologiya fanlaridan maxsus xonalar tashkil qilingan. O’quv mashg‘ulotlari malakali o’qituvchilar tomonidan maxsus jixozlangan auditoriyalarda о‘tkaziladi.</p>
<p align="justify">Kollejdagi yangiliklar bilan tanishtirib borish uchun radiomarkaz ishlaydi. Shuningdek kollej faoliyati ushbu sayitda yoritib boriladi.</p>
<p align="justify">Kollejda о‘qituvchilarning axborot texnalogiyalarni yaxshi o’zlashtirishlari uchun kompiyuter siniflari faoliyat kо‘rsatadi. Ushbu siniflarda zamonaviy kompiyuterlar o’rnatilgan bo’lib, ularda turli o’quv mashg’ulotlarini о‘tkazishda foydalanib kelinmoqda.</p>
<p align="justify">2010-2011 o’quv yilida Buyuk Britaniya konsulligi tomonidan o’tkazilgan “Eng ilg’or Innovatsion g’oya” ко‘rik tanlovida о‘quvchilarimiz oliy о‘rinni olishdi va 2011 yil Angliyada bo’ladigan xalqoro tanlovga qatnashish yo‘llanmasini qo’lga kiritdi.</p>
<p align="justify">Kollejda xar oyda turli xil mazmundagi uchrashuv va tad birlarni о‘z ichiga olgan kafedra oyliklari о‘tqaziladi.</p>
<p align="justify">О‘quv yurtida fan, madaniyat arboblari, huquq-tartibot organlari vakillari, mа‘naviyat va mа‘rifat markazi, diniy idora vakillari, maxalla faollari bilan uchrashuvlar, tarbiyaviy axamiyatga ega bo‘lgan kinofilim tomoshalari, teatr, muzey, ko’rgazmalarga ekskursiyalar tashkil qilinadi.</p>


